CREATE TABLE DIRECCION(
CODIGO BIGINT PRIMARY KEY,
DIRECCION VARCHAR(50),
POBLACION VARCHAR(50),
CODIGO_POSTAL VARCHAR(50),
PROVINCIA VARCHAR(50),
PAIS VARCHAR(50)
);

CREATE TABLE PRODUCTO(
CODIGO BIGINT PRIMARY KEY,
NOMBRE VARCHAR(50),
PRECIO DOUBLE,
FECHA_ALTA TIMESTAMP,
FAMILIA ENUM('HARDWARE', 'SOFTWARE','CONSUMIBLES','BEBIDAS'),
DESCATALOGADO BOOLEAN
);

CREATE TABLE COMERCIAL(
CODIGO BIGINT PRIMARY KEY,
NOMBRE VARCHAR(50),
APELLIDO1 VARCHAR(50),
APELLIDO2 VARCHAR(50)
);

CREATE TABLE CLIENTE(
IDENTIFICADOR_FISCAL VARCHAR(50) PRIMARY KEY,
NOMBRE_COMERCIAL VARCHAR(50),
NOMBRE VARCHAR(50),
CODIGO_DIRECCION BIGINT REFERENCES DIRECCION(CODIGO),
CODIGO_COMERCIAL BIGINT REFERENCES COMERCIAL(CODIGO),
APELLIDO1 VARCHAR(50),
APELLIDO2 VARCHAR(50),
TELEFONO1 VARCHAR(50),
TELEFONO2 VARCHAR(50),
EMAIL VARCHAR(50)
);

CREATE TABLE CLIENTE_PRODUCTOS(
PRODUCTO_CODIGO BIGINT,
CLIENTE_IDENTIFICADOR_FISCAL VARCHAR(50),
FOREIGN KEY (PRODUCTO_CODIGO) REFERENCES PRODUCTO(CODIGO),
FOREIGN KEY (CLIENTE_IDENTIFICADOR_FISCAL) REFERENCES CLIENTE (IDENTIFICADOR_FISCAL),
PRIMARY KEY(PRODUCTO_CODIGO, CLIENTE_IDENTIFICADOR_FISCAL)
);
